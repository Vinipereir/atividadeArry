//Comando includes
// Determina se um array contém um determinado elemento

//retornando true ou false conforme apropriado

const array = [1, 2, 3];
console.log(array.includes(2)); // Saída: true
console.log(array.includes(4)); // Saída: false

//utilizando um array de frutas

const fruits = ['Maçã', 'Banana', 'Laranja'];
console.log(fruits.includes('Banana')); // Saída: true
console.log(fruits.includes('Morango')); // Saída: false
